public class NumberTriangle{
public static void main(String[] args){   
for(int i = 1; i < 8; i++){
 for(int j = 0; j < i; j++){
     System.out.print(i);
 }
    System.out.print("\n");
}

for(int m = 1; m < 4; m ++){
 for(int n = 0; n < 10; n ++){
     for(int p = 1; p < 4; p ++){
  System.out.print(n);   
     }
 }
  System.out.print("\n");   
}
}
}
